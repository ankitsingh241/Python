# 1. if-elif-else ladder in Python

a = 15
if(a>5):
    print("The value of a is greater than 5.")
elif(a>12):
    print("The value of a is greater than 12.")
elif(a>21):
    print("The value of a is greater than 21.")
else:
    print("The value is not greater than 5,12 or 21.")

# else is optional.

