# It is used to generate a sequence of numbers.
# range(start, stop, step-size)

for i in range(8):
    print(i)        # It will print from 0 to 7.

for i in range(1, 8):
    print(i)        # It will print from 1 to 7.

for i in range(1, 8, 2):
    print(i)        # It will print after 2 steps.

    