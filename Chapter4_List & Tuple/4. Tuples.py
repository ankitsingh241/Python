# A tuple is immutable data type in python.

# Create a Tuple
t = (1, 2, 3, 4, 5)
print(t)

# Cannot update the values of tuple, it will show error.
t[2] = 25
print(t)

# t1 = () # Empty tuple
# t1 = (1) # Wrong way to declare a Tuple with Single element
t1 = (1,) # Tuple with Single element, need comma(,).
print(t1)

