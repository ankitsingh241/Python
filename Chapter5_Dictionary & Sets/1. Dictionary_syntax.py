# Dictionary is a collection of key-value pairs

#        Keys         : Values
dict = {
        "Slow"        : "taking a lot of time to do any work",
        "Ankit"       : "Permanent print",
        "F1"          : "Fast cars",
        "list"        : [1,2,3,4,5],
        "anotherdict" : {'Ankit' : 'Cloud architect'}
       }

# It can not contain duplicate keys.
# It is mutable.

print(dict["F1"])
print(dict['anotherdict']['Ankit'])

